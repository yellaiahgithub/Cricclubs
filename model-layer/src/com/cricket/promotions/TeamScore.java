package com.cricket.promotions;

public class TeamScore {
	
	private String runs;
	private String wickets;
	private String completed_overs;
	private String current_over_balls;
	
	public String getRuns() {
		return runs;
	}
	public void setRuns(String runs) {
		this.runs = runs;
	}
	public String getWickets() {
		return wickets;
	}
	public void setWickets(String wickets) {
		this.wickets = wickets;
	}
	public String getCompleted_overs() {
		return completed_overs;
	}
	public void setCompleted_overs(String completed_overs) {
		this.completed_overs = completed_overs;
	}
	public String getCurrent_over_balls() {
		return current_over_balls;
	}
	public void setCurrent_over_balls(String current_over_balls) {
		this.current_over_balls = current_over_balls;
	}
	
	
	
	
	
	
}
